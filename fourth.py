#WHILE LOOP
#allows you to execute a code repeatedly until a condition is false

i = 1
while i <= 10:
    print(i)
    i = i + 1 #the shorthand is i += 1
print("done")
#the code will continue to run till it gets to the number 10


#FOR LOOP
for letter in "Giraffe Academy":
    print(letter)
#means print every letter in giraffe academy until it is out of letters
# you can use this loop for arrays as well
#you can use for ... in range( , ) to perform a function on a range of items

